/**
 *
 * An enumeration that represents the possible speeds that actions can have in the game. The only members are QUICK and FULL, corresponding to quick and full actions, respectively.
 */
public enum ActionSpeed {
    /**
     * This member is for actions that are quick actions.
     */
    QUICK,
    /**
     * This member is for actions that are full actions.
     */
    FULL
}
