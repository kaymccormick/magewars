/**
 * Enumeration that represents the various "Traits" that exist in the game system.
 */
public enum Trait {
    /***
     * Flying trait.
     */
    FLYING,
    /***
     * Flame immunity trait.
     */
    FLAME_IMMUNITY,
    /***
     * Legendary trait
     */
    LEGENDARY,
    /***
     * Fast trait.
     */
    FAST,
    /***
     * Pest trait.
     */
    PEST,
    /***
     * Elusive trait.
     */
    ELUSIVE,
    /***
     * Slow trait.
     */
    SLOW,
    /***
     * Burnproof trait.
     */
    BURNPROOF,
    /***
     * Non-living trait.
     */
    NONLIVING,
    /***
     * Umovable trait.
     */
    UNMOVABLE,
    /***
     * Familiar trait. This is a significant trait because familiars are spell-casters.
     */
    FAMILIAR,
    /***
     * Magecast trait.
     */
    MAGECAST,
    /***
     * Mindmage-only trait.
     */
    MINDMAGE_ONLY,
    /***
     * Novice trait.
     */
    NOVICE,
    /***
     * Epic trait.
     */
    EPIC,
    /***
     * Holymage trait.
     */
    HOLYMAGE_ONLY,
    /***
     * Forcemaster trait.
     */
    FORCEMASTER_ONLY,
    /***
     * Zone-exclusive trait.
     */
    ZONE_EXCLUSIVE,
    /***
     * Beastmaster-only trait.
     */
    BEASTMASTER_ONLY,
    /***
     * Living trait.
     */
    LIVING,
    /***
     * Hydro-immunity trait.
     */
    HYDRO_IMMUNITY,
    /***
     * Incorporeal trait.
     */
    INCORPOREAL,
    /***
     * Autonomous trait.
     */
    AUTONOMOUS,
    /***
     * Spawnpoint trait.
     */
    SPAWNPOINT
}
